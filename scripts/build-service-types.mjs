import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises'
import { basename, dirname, extname, join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = fileURLToPath(new URL('..', import.meta.url))
const servicesDirectory = join(root, 'src', 'services')
const outputPath = join(root, 'dist', 'services.d.ts')

async function findDeclarationFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true })
  const paths = []
  for (let i = 0; i < entries.length; i++) {
    const path = join(directory, entries[i].name)
    if (entries[i].isDirectory()) {
      paths.push(...await findDeclarationFiles(path))
    } else if (extname(entries[i].name) === '.ts' && entries[i].name.endsWith('.d.ts')) {
      paths.push(path)
    }
  }
  return paths
}

const declarationPaths = await findDeclarationFiles(servicesDirectory)
declarationPaths.sort((left, right) => relative(servicesDirectory, left).localeCompare(
  relative(servicesDirectory, right),
))

const declarations = []
const exportedNames = new Map()
for (let i = 0; i < declarationPaths.length; i++) {
  const path = declarationPaths[i]
  const source = (await readFile(path, 'utf8')).trim()
  const displayPath = relative(root, path)

  if (/^\s*import\s/m.test(source) || /^\s*export\s+(?:\*|\{)[^\n]*\sfrom\s/m.test(source)) {
    throw new Error(`${displayPath} must not import or re-export declarations from another service`)
  }

  const exportPattern = /^export\s+(?:declare\s+)?(?:abstract\s+)?(?:type|interface|class|const|function|enum|namespace)\s+([A-Za-z_$][\w$]*)/gm
  for (let match = exportPattern.exec(source); match; match = exportPattern.exec(source)) {
    const existingPath = exportedNames.get(match[1])
    if (existingPath && existingPath !== displayPath) {
      throw new Error(`Duplicate exported declaration ${match[1]} in ${existingPath} and ${displayPath}`)
    }
    exportedNames.set(match[1], displayPath)
  }

  const serviceName = basename(path, '.d.ts')
  const section = `/* ==========================================================================
 * Service: ${serviceName}
 * Source: ${displayPath}
 * ========================================================================== */`
  declarations.push(`${section}

${source}`)
}

const header = `/**
 * Generated by \`make types\` from declaration files under \`src/services/\`.
 * Edit the source service declarations, not this file.
 */`

await mkdir(dirname(outputPath), { recursive: true })
await writeFile(outputPath, `${header}\n\n${declarations.join('\n\n')}\n`)
console.log(`Built ${relative(root, outputPath)} from ${declarationPaths.length} service declaration files.`)
